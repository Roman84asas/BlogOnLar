    public function collections()
    {
       $result = [];

       $eloquenCollection = BlogPost::withTrashed()->get();
       $collection = collect($eloquenCollection->toArray());

       $result['first'] = $collection->first();
       $result['last'] = $collection->last();

       $result['where']['data'] = $collection
           ->where('catalog_id', 10)
           ->values()
           ->keyBy('id');
    }

